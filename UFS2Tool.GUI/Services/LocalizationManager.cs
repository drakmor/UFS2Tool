    [ObservableProperty]
    private string _currentLanguage = "English";

    public LocalizationManager()
    {
        var detectedLanguage = DetectLanguageFromLocale();
        _currentLanguage = detectedLanguage;
        _strings = GetStringsForLanguage(detectedLanguage);
    }

    private static string DetectLanguageFromLocale()
    {
        var culture = CultureInfo.CurrentUICulture;
        var twoLetter = culture.TwoLetterISOLanguageName;
        var name = culture.Name;

        return name switch
        {
            "pt-BR" => "Português (Brasil)",
            "zh-TW" or "zh-Hant" => "中文 (繁體)",
            "zh-Hans" => "中文 (简体)",
            _ => twoLetter switch
            {
                "fr" => "Français",
                "es" => "Español",
                "de" => "Deutsch",
                "zh" => "中文 (简体)",
                "ja" => "日本語",
                "ar" => "العربية",
                "ru" => "Русский",
                "hi" => "हिन्दी",
                "ko" => "한국어",
                "tr" => "Türkçe",
                "it" => "Italiano",
                "pt" => "Português",
                "sv" => "Svenska",
                "fi" => "Suomi",
                "pl" => "Polski",
                "vi" => "Tiếng Việt",
                "ro" => "Română",
                "uk" => "Українська",
                "cs" => "Čeština",
                "th" => "ไทย",
                "nl" => "Nederlands",
                "id" => "Bahasa Indonesia",
                "hu" => "Magyar",
                _ => "English",
            }
        };
    }

